/* Includes */
#include "system.h"
#include "VCP.h"

int main()
{
  systemInit();

  VCP vcp(serial_config[VCP_INDEX]);

  while(1)
  {
    uint8_t hello_string[] = "hello world!\n";
    vcp.write(hello_string, 13);
    delay(200);
  }
}
